#sortの操作
my @result = sort 1, 2, 3, 4, 5, 9, 12, 35;     #1 12 2 3 35 4 5 6 9

my @result2 = sort {
    if ( $a < $b ) { -1 }   #$aを$bより前に配置したい場合-1
    elsif ( $a > $b ) { +1 }    #$bを$aより前に配置したい場合+1
    else { 0 }  #どっちでも構わない場合０
} 1, 2, 3, 4, 5, 9, 12, 35;     #1 2 3 4 5 9 12 35

my @result3 = sort { $a <=> $b } 1, 2, 3, 4, 5, 9, 12, 35;    #1 2 3 4 5 9 12 35